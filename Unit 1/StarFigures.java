public class StarFigures {
public static void main(String[] args) {
        twobar();
        cross();
        System.out.println("");
        twobar();
        cross();
        twobar();
        System.out.println("");
        lines();
        twobar();
        cross();
}
public static void twobar(){
        System.out.println("*****\n*****");
}
public static void cross(){
        System.out.println(" * *\n  *\n * *");
}
public static void lines(){
        System.out.println("  *\n  *\n  *");
}
}
